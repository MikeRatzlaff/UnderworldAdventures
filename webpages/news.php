<h1>Underworld Adventures</h1>

<p>
Welcome to the Underworld Adventures home page. Underworld Adventures is a project to recreate Ultima Underworld 1 on modern operating systems (e.g. Win32, Linux or MacOS), using the original game files. It uses OpenGL for 3D graphics, the SDL library for platform specific tasks and Lua for scripting. It is developed as Open Source and is licensed under the GNU General Public License.
</p>

<h2>project news</h2>

<span class="newsHeading">2004-04-10 - new release: uwadv 0.9 "mojito"</span>

<p class="newsBody">
I'm happy to announce that after *mumble,mumble* months, we have released a new version of Underworld Adventures!<sup>[1]</sup>. This release codenamed after the cuban national drink doesn't have new features added. In fact it misses some features that were present in 0.8, but the source code was heavily restructured and refactored. Source code and binaries for Windows and Linux are available on the <a href="index.php?page=download">download page</a>. From now on releases will be done more frequently. It is planned to have a release at least every 2 months.
</p>

<p class="newsBody">
We also released a new version of the digital music soundtrack pack that contains the 2 new and unreleased tracks "Wanderer" and "Maps & Legends" from Martin Gircys-Shetty (QQtis). The package is also available on the <a href="index.php?page=download">download page</a>. Thanks Martin!
</p>

<p class="newsBody">
The project always searches for people to help out coding, porting and testing. We especially search for someone who ports the code to Mac OS X. Also some artwork is needed for the splash screen and a new installer. Please see the <a href="index.php?page=dev">development</a> section for more information.
</p>

<p class="newsBody">
We also wish the Underworld Adventures a happy 2nd birthday anniversary! Two years ago I got the mail from SourceForge that my project is approved. Although readers of the <a href="index.php?page=devlog">dev log</a> know that the idea of the project emerged earlier, it's nevertheless a good date to release a new version. So enjoy this release!
</p>

