</p>

<p>
Exultbot is responsible for the <a href="http://www.math.leidenuniv.nl/~wpalenst/uwadvlog.php" target="_blank">#uwadv channel logs</a>. It also understands some commands. Just say "?help" in the channel to get an overview of the commands.
</p>

